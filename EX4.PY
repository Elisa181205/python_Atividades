# Faça um programa que peça as 4 notas bimestrais e mostre a média
prim_Bim = int(input("Insira a nota do Primeiro Bimestre : "))
segu_Bim = int(input("Insira a nota do Segundo Bimestre : "))
terc_Bim = int(input("Insira a nota do Terceiro Bimestre : "))
quar_Bim = int(input("Insira a nota do Quarto Bimestre : "))

media = (prim_Bim+segu_Bim+terc_Bim+quar_Bim)/4

print(media)